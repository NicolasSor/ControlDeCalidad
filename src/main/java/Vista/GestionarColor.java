//Tabla
    @Override
    public void ActualizarTabla() {
    {
        DefaultTableModel matriz= new DefaultTableModel();       
        matriz.addColumn("Codigo");
        matriz.addColumn("Descripcion");
        for(Color c : Empresa.getCol()){
            Object []f = new Object[2];
            f[0]= c.getCodigoCol();
            f[1]= c.getDescripcionCol();
            matriz.addRow(f);
        }
        tablaColor.setModel(matriz);
    }
    }


