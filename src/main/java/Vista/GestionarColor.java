    private void BorrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BorrarActionPerformed
        if (tablaColor.getSelectedRow() <=-1)
            mostrarMensajeErrorFila();
        else
            {
            Object[] o = new Object[2];
            o[0] = tablaColor.getValueAt(tablaColor.getSelectedRow(), 0);
            o[1] = tablaColor.getValueAt(tablaColor.getSelectedRow(), 1);
            Color col = new Color((int) o[0], (String) o[1]);
            presentador.borrarColor(col);
            }  
    }//GEN-LAST:event_BorrarActionPerformed

